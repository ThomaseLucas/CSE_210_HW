namespace Develop03
{
public class Word
{
    public string _scriptureText { get; set; }
    public bool _isHidden { get; set; }

    public string GetScripture(string scripture)
    {
        return scripture = _scriptureText;
    }

    public bool GetIsHidden(bool hidden)
    {
        return hidden = _isHidden;
    }
}
}
